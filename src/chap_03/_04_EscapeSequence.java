package chap_03;

public class _04_EscapeSequence {
    public static void main(String[] args) {
        System.out.println("go");
        System.out.println("la");

        System.out.println("go\nla dsa");

        System.out.println("kim\t\t200$");
        System.out.println("nora\t\t300$");
        System.out.println("your name is \"min\"");

    }
}
